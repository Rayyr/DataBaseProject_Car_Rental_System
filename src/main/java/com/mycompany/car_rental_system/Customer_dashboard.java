          public void set_booking_count_label(int cus_id){
       
        make_connector();
        int booking_count=0;

        try {

            con.setAutoCommit(false);
            String q = "select COUNT(bookingid) AS booking_count_for_this_cus from booking where booking.customerid='"+cus_id+"'";//by default tehy are unique so there is no need to use ditinict
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery(q);

            //to fetch the rows 
            while (r.next() == true) {

               booking_count=r.getInt("booking_count_for_this_cus");
             

            }

            con.commit();
            con.close();
            con = null;

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }
        
        this.booking_count_label.setText(String.valueOf(booking_count));
        
       
   }
       
       
       
       
    public void change_wellcome_label_to_username(int cus_id){
        
       
        make_connector();
        
        try {

            // first we need to check if it is the manger , we sure that only we have 1 manger 
            con.setAutoCommit(false);
            String q = "Select fname,lname from customer where customerid='"+cus_id+"'";
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery(q);
            //the result will be only 1 row 
            r.next();
          
            
             this.wellcome_label.setText("Wellcome "+r.getString("fname")+" "+r.getString("lname"));
            
            con.commit();
            con.close();
            con = null;
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            this.dispose();
        }
        
    }
       
       
       
