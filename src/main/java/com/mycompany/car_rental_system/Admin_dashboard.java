    private void income_report_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_income_report_btnActionPerformed
        // create the income report 

        make_connector();

        try {

            con.setAutoCommit(false);

            InputStream in_file=new FileInputStream(new File("Blank_A4.jrxml"));
            JasperDesign jd=JRXmlLoader.load(in_file);
            JasperReport jr=JasperCompileManager.compileReport(jd);
            JasperPrint jp=JasperFillManager.fillReport(jr,null,con); 
        
           
            
            JFrame jf=new JFrame("new Report");
            jf.getContentPane().add(new JRViewer(jp));
            jf.pack();
            jf.setVisible(true); 
            
            con.commit();
            con.close();
            in_file.close();
            con = null;

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }

    }//GEN-LAST:event_income_report_btnActionPerformed

