    public void set_car_count_label() {

        make_connector();
        int car_count = 0;

        try {

            con.setAutoCommit(false);
            String q = "select COUNT(veichleid) AS car_count from veichle";//by default tehy are unique so there is no need to use ditinict
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery(q);

            //to fetch the rows 
            while (r.next() == true) {

                car_count = r.getInt("car_count");

            }

            con.commit();
            con.close();
            con = null;

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }

        this.cars_count_label.setText(String.valueOf(car_count));

    }

