    public void fill_features() {

        if (this.jrb_air.isSelected() == true) {
            feature[0] = this.jrb_air.getActionCommand();
        }

        if (this.jrb_bluetooth.isSelected() == true) {
            feature[1] = this.jrb_bluetooth.getActionCommand();
        }

        if (this.jrb_heatedseat.isSelected() == true) {
            feature[2] = this.jrb_heatedseat.getActionCommand();
        }

        if (this.jrb_sunroof.isSelected() == true) {
            feature[3] = this.jrb_sunroof.getActionCommand();
        }

        if (this.jrb_gps.isSelected() == true) {
            feature[4] = this.jrb_gps.getActionCommand();
        }

        if (this.jrb_parkingcam.isSelected() == true) {
            feature[5] = this.jrb_parkingcam.getActionCommand();
        }

        return;
    }

    public boolean verify_selection() {

        if ((this.bg_geer.getSelection() == null) || (this.bg_purpose.getSelection() == null)   ) {
            return true; //not selected buttons 
        }
        return false;//selected ones

    }

    public void reinitialize_the_feilds() {

        this.jtf_color.setText(null);
        this.jtf_model.setText(null);
        this.jtf_price.setText(null);
        this.bg_geer.clearSelection();
        this.bg_purpose.clearSelection();
      
        this.jrb_air.setSelected(false);
        this.jrb_bluetooth.setSelected(false);
        this.jrb_heatedseat.setSelected(false);
        this.jrb_sunroof.setSelected(false);
        this.jrb_gps.setSelected(false);
        this.jrb_parkingcam.setSelected(false);

        return;
    }

    public boolean verify_strings(String x) {

        if ((x.contains(" ") == true) || (x.isEmpty() == true)) {
            return true;//has spaces
        }
        return false;// not has spaces

    }

    public int get_brand_id(String brandname2) {

        make_connector();
        int ind = 0;

        try {

            con.setAutoCommit(false);

            String q = "select veichletypeid from veichletype where brandname='" + brandname2 + "'";
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery(q);

            r.next();
            ind = r.getInt("veichletypeid");

            con.commit();
            con.close();
            con = null;

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }

        return ind;

    }

